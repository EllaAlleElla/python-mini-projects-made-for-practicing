print("你好，这是一个关于变量、输入、输出的教学。\n")
print("首先，如果你想让用户看见你说了哪句话，你需要以print后面跟英文括号的形式写入代码。\n")
print("括号里可以放的东西有两种：纯文本和变量。\n")
print("首先是纯文本。纯文本不能直接放在print后面接的括号里，需要在括号里打一个英文引号包裹住文本内容。\n")
print("然后是变量。变量需要提前准备。在print代码的前一行写上xxx=yyy，xxx就是这个变量的名称，只在源代码里能找到，yyy就是变量的内容。\n")
print("打个比方，变量就像盒子。yyy是盒子里装的东西，是你真正会用到的内容，而xxx是贴在盒子上的标签，让你能分辨不同盒子都装着什么，哪个盒子是你需要的。\n")
print("举个例子。如果你写进了这样一串代码：“玉皇大帝=小明”，那么你可以这样用到它：\n")
print("输入第二行代码print【玉皇大帝】，用户运行程序后就会看到屏幕上出现“小明”两个字。*注：此处为了避免混淆使用了方括号，自己写代码时请换成正常英文小括号。\n")
print("如果你写成了print【小明】，那什么都不会发生。因为并没有名为小明的变量，只有名为玉皇大帝的变量。代码只认识玉皇大帝，用户只可能见到小明。\n")
print("而如果你写成print【“玉皇大帝=小明”】，用户就会看到中文双引号内的文本内容，也就是玉皇大帝=小明。注意，和方括号同理，自己写时请写成英文傻瓜引号，不要使用中文标点。\n")
print("你也可以把纯文本和变量混合起来使用。例如，这行代码：print【“你好，”+玉皇大帝】，可以让用户看见：你好，小明。\n")
print("input还有一个用法，那就是让用户来决定xxx=yyy中的yyy是什么。让用户自己来填充盒子，你只需要给盒子写好标签。\n")
print("具体实现方法是这样的：假设你给变量起名叫太上老君，而你想让用户决定这个太上老君实际上是谁。那么你就可以输入 太上老君=【“你觉得谁才是太上老君？”】，用户就会看到自己的屏幕上有一句话：你觉得谁才是太上老君？，之后不管他们输入什么内容，按回车后都会被记录成太上老君变量的内容。\n")
print("假如用户输入 太上老君本尊 并按下回车，那么这个变量在本地会变成 太上老君=太上老君本尊。之后你用print代码输出变量的内容，用户能看到的也是 太上老君本尊 。\n")
